class ArithmeticOper
{
	public static void main(String [] args)
	{
		int p = 15, q=3;
	
		System.out.println("Arithmetic operator Addition");
		System.out.println("p+q ="+(p+q));
	
		System.out.println("Arithmetic operator Substraction");
		System.out.println("p-q ="+(p-q));
	
		System.out.println("Arithmetic operator multiplication");
		System.out.println("p*q ="+(p*q));
	
		System.out.println("Arithmetic operator division");
		System.out.println("p/q ="+(p/q));
	
		System.out.println("Arithmetic operator modulas");
		System.out.println("p%q ="+(p%q));
	}
	

}